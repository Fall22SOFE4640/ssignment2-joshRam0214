class MyHomePage extends StatefulWidget {
  const MyHomePage({super.key, required this.title});

  final String title;

  @override
  State<MyHomePage> createState() => _MyHomePageState();
}

class _MyHomePageState extends State<MyHomePage> {
  int _counter = 0;

  void _incrementCounter() {
    setState(() {
      _counter++;
    });
  }

  @override
  Widget build(BuildContext context) {

    return Scaffold(
      appBar: AppBar(

        title: Text(widget.title),
      ),
      body: Center(
        child: Column(

          mainAxisAlignment: MainAxisAlignment.center,
          children: <Widget>[
            const Text(
              'You have pushed the button this many times:',
            ),
            Text(
              '$_counter',
              style: Theme.of(context).textTheme.headline4,
            ),
          ],
        ),
      ),
      floatingActionButton: FloatingActionButton(
        onPressed: _incrementCounter,
        tooltip: 'Increment',
        child: const Icon(Icons.add),
      ), // This trailing comma makes auto-formatting nicer for build methods.
    );
  }
}
void callCampsite (){
  const restNumber = '18002252619';
  FlutterPhoneDirectCaller.callNumber(restNumber);
}
void shareCampsite(){
  Share.share('Oeschinen Lake Campground, Kandersteg Switzerland');
}
void routeToCampsite(){
  MapsLauncher.launchQuery('Oeschinensee, 3718 Kandersteg, Switzerland');
  MapsLauncher.launchCoordinates(46.49928574733053, 7.72906662399375);
}

